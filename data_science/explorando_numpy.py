import numpy as np

# Criando um array numpy
arr = np.array([1, 2, 3, 4, 5])

# Imprimindo o array
print(arr)

# Realizando operações com o array
soma = np.sum(arr)
media = np.mean(arr)
maximo = np.max(arr)
minimo = np.min(arr)

# Imprimindo os resultados
print("Soma:", soma)
print("Média:", media)
print("Máximo:", maximo)
print("Mínimo:", minimo)